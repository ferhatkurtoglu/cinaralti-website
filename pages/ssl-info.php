                    <h4 class="mt-4">SSL Sertifikası Nedir?</h4>
                    <p>SSL (Secure Sockets Layer), internet üzerinden iletilen verileri şifreleyerek, bilgilerinizin
                        güvenliğini sağlayan bir güvenlik protokolüdür. SSL sertifikası, sitemize gönderilen tüm
                        bilgilerin (kredi kartı bilgileri, kişisel bilgiler vb.) şifrelenerek iletilmesini sağlar. Bu
                        şekilde bilgileriniz, kötü niyetli kişilerin eline geçmesi veya değiştirilmesi engellenir.</p>

                    <h4 class="mt-4">SSL Sertifikası Nasıl Çalışır?</h4>
                    <p>SSL, web siteniz ile ziyaretçinin tarayıcısı arasındaki bağlantıyı güvenli hale getirir. Bu
                        güvenli bağlantı sayesinde:</p>
                    <ul>
                        <li>Gönderilen bilgiler şifrelenir ve üçüncü şahıslar tarafından okunamaz hale gelir.</li>
                        <li>Bilgilerin bütünlüğü korunur, yani iletim sırasında değiştirilmesi engellenir.</li>
                        <li>Web sitesinin kimliği doğrulanır, böylece kullanıcılar gerçek siteyle iletişim
                            kurduklarından emin olurlar.</li>
                    </ul>

